public class Paquete {
   public String id_paquete;
   public String nom_paquete;
   public String id_pizza;
   public String id_productoextra;
   public String precio_paquete;

    public String getId_paquete() {
        return id_paquete;
    }

    public void setId_paquete(String id_paquete) {
        this.id_paquete = id_paquete;
    }

    public String getNom_paquete() {
        return nom_paquete;
    }

    public void setNom_paquete(String nom_paquete) {
        this.nom_paquete = nom_paquete;
    }

    public String getId_pizza() {
        return id_pizza;
    }

    public void setId_pizza(String id_pizza) {
        this.id_pizza = id_pizza;
    }

    public String getId_productoextra() {
        return id_productoextra;
    }

    public void setId_productoextra(String id_productoextra) {
        this.id_productoextra = id_productoextra;
    }

    public String getPrecio_paquete() {
        return precio_paquete;
    }

    public void setPrecio_paquete(String precio_paquete) {
        this.precio_paquete = precio_paquete;
    }
           

    
}
