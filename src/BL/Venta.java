package BL;

/**
 *
 * @author Katia Salazar
 */
public class Venta {
     public String id_venta;
     public String fecha_venta;
     public String nom_comprador;
     public String nom_producto;
     public int cantidad;
     public double total;

    public String getId_venta() {
        return id_venta;
    }

    public void setId_venta(String id_venta) {
        this.id_venta = id_venta;
    }

    public String getFecha_venta() {
        return fecha_venta;
    }

    public void setFecha_venta(String fecha_venta) {
        this.fecha_venta = fecha_venta;
    }

    public String getNom_comprador() {
        return nom_comprador;
    }

    public void setNom_comprador(String nom_comprador) {
        this.nom_comprador = nom_comprador;
    }

    public String getNom_producto() {
        return nom_producto;
    }

    public void setNom_producto(String nom_producto) {
        this.nom_producto = nom_producto;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }

    
     
}
